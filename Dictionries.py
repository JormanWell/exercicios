# DICIONÁRIOS
# DICT[key] --> value
# {key1: value1, key2: value2, ...}

codigo_postal = {
    'buraca': '2610',
    'damaia': '2720',
    'carnaxide': '2970'
    }

print(codigo_postal['buraca'])
# 2610

